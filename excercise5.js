var x = 20;
var y = 10;
var oper = '+';
if (oper == '+') {
    console.log(`sum of numbers is  ${x + y}`);
}
if (oper == '-') {
    console.log(`difference of numbers is ${x - y}`);
}
if (oper == '*') {
    console.log(`product of numbers is ${x * y}`);
}
if (oper == '/') {
    console.log(`division of numbers is ${x / y}`);
}
if (oper == '%') {
    console.log(`modular division is ${x % y}`);
}